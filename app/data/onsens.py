def iter_matching_onsens(tags: Iterable[str]) -> list[OnsenEntry]:
    tag_set = set(tags)
    matched = [entry for entry in ONSEN_CATALOG if tag_set.intersection(entry.ideal_for)]
    return matched or list(ONSEN_CATALOG)


def iter_matching_variants(entry: OnsenEntry, tags: Iterable[str]) -> list[OnsenVariant]:
    tag_set = set(tags)
    matched = [variant for variant in entry.variants if tag_set.intersection(variant.ideal_for)]
    return matched or list(entry.variants)


def find_onsen(slug: str) -> OnsenEntry | None:
    for entry in ONSEN_CATALOG:
        if entry.slug == slug:
            return entry
    return None


def find_variant(entry: OnsenEntry, variant_slug: str | None) -> OnsenVariant:
    if variant_slug:
        for variant in entry.variants:
            if variant.slug == variant_slug:
                return variant
    return entry.variants[0]
